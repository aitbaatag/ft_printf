#include "ft_printf.h"

int	ft_putstr_p(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "0x0", 3);
		return (3);
	}
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
	return (i);
}
